/*
 * Transformation matrix for Rijndael's mix column operation which is the
 * the result of multiplying a bytes representative polynomial over the 
 * field Gf(2^8) with irreducible polynomial of coefficients 
 */

/*
 * Implementation of Rijndael S-Box.
 */
uint8_t sBox(const unsigned char byte) {
    return s[byte];
}

uint8_t sBoxInv(const unsigned char byte) {
    return inv_s[byte];
}

void subBytes(unsigned char state[]) {
    int i;
    for (i = 0; i < BLOCK_SIZE; i++) {
        state[i] = sBox(state[i]);
    }
}

void subBytesInv(unsigned char state[]) {
    int i;
    for (i = 0; i < BLOCK_SIZE; i++) {
        state[i] = sBoxInv(state[i]);
    }
}

