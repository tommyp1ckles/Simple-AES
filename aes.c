void shiftRow(unsigned char *state, int s) {
    unsigned char tmp[BLOCK_SIZE_WORD];
    int i, k;
    for (i = 0; i < BLOCK_SIZE_WORD; i++) {
        tmp[i] = state[(i + s) % BLOCK_SIZE_WORD];
    }
    for (i = 0; i < BLOCK_SIZE_WORD; i++)
        state[i] = tmp[i];
}

void shiftRowInv(unsigned char *state, int s) {
    unsigned char tmp[BLOCK_SIZE_WORD];
    int i, k;
    for (i = 0; i < BLOCK_SIZE_WORD; i++) {
        tmp[(i + s) % BLOCK_SIZE_WORD] = state[i];
    }
    for (i = 0; i < BLOCK_SIZE_WORD; i++)
        state[i] = tmp[i];
}

void shiftRows(unsigned char state[], int s) {
    int i;
    for (i = 0; i < BLOCK_SIZE_WORD; i++)
        shiftRow(&state[i * BLOCK_SIZE_WORD], i);
}
unsigned char gf_multiply(unsigned char a, unsigned char b) {
    unsigned char p = 0;
    unsigned char counter;
    unsigned char carry;
    for (counter = 0; counter < 8; counter++) {
        if (b & 1) 
            p ^= a;
        carry = a & 0x80;  /* detect if x^8 term is about to be generated */
        a <<= 1;
        if (carry) 
            a ^= 0x1B; /* replace x^8 with x^4 + x^3 + x + 1 */
        b >>= 1;
    }
    return p;
}



unsigned char gf_add(unsigned char a, unsigned char b) {
    return a ^ b;
}

void mixColumns(unsigned char *state) {
    int i;
    unsigned char a, b, c, d;
    for (i = 0; i < 4; i++) {
        a = state[i];
        b = state[i + 4];
        c = state[i + 8];
        d = state[i + 12];

        state[i] = gf_multiply(a, 2) ^ gf_multiply(b, 3) ^ gf_multiply(c, 1) ^
            gf_multiply(d, 1);
        state[i + 4] = gf_multiply(a, 1) ^ gf_multiply(b, 2) ^ gf_multiply(c, 3) ^
            gf_multiply(d, 1);
        state[i + 8] = gf_multiply(a, 1) ^ gf_multiply(b, 1) ^ gf_multiply(c, 2) ^
            gf_multiply(d, 3);
        state[i + 12] = gf_multiply(a, 3) ^ gf_multiply(b, 1) ^ gf_multiply(c, 1) ^
            gf_multiply(d, 2);
    }
}

void mixColumnsInv(unsigned char *state) {
    int i;
    unsigned char a, b, c, d;
    for (i = 0; i < 4; i++) {
        a = state[i];
        b = state[i + 4];
        c = state[i + 8];
        d = state[i + 12];

        state[i] = gf_multiply(a, 14) ^ gf_multiply(b, 11) ^ gf_multiply(c, 13) ^
            gf_multiply(d, 9);
        state[i + 4] = gf_multiply(a, 9) ^ gf_multiply(b, 14) ^ gf_multiply(c, 11) ^
            gf_multiply(d, 13);
        state[i + 8] = gf_multiply(a, 13) ^ gf_multiply(b, 9) ^ gf_multiply(c, 14) ^
            gf_multiply(d, 11);
        state[i + 12] = gf_multiply(a, 11) ^ gf_multiply(b, 13) ^ gf_multiply(c, 9) ^
            gf_multiply(d, 14);
    }
}

void addRoundKey(unsigned char *state, unsigned char *roundKey) {
    int i;
    for (i = 0; i < 16; i++)
        state[i] ^= roundKey[i];
}

unsigned char *rotWord(unsigned char *word) {
    unsigned char *newWord;
    newWord = malloc(4);
    newWord[0] = word[1];
    newWord[1] = word[2];
    newWord[2] = word[3];
    newWord[3] = word[0];
    return newWord;
}

void subWord(unsigned char *word) {
    int i;
    for (i = 0; i < 4; i++)
        word[i] = sBox(word[i]);
}

